const dataset = [];
const startDate = new Date(1970, 0, 1);
const endDate = new Date(2005, 11, 31);

function getRandomDate(start, end) {
  const date = new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()));
  const yyyy = date.getFullYear();
  const mm = String(date.getMonth() + 1).padStart(2, '0');
  const dd = String(date.getDate()).padStart(2, '0');
  const hh = String(Math.floor(Math.random() * 24)).padStart(2, '0');
  const min = String(Math.floor(Math.random() * 60)).padStart(2, '0');
  return `${yyyy}-${mm}-${dd} ${hh}:${min}`;
}

Object.entries(categories).forEach(([key, details]) => {
  for (let i = 0; i < details.count; i++) {
    dataset.push({
      birth: getRandomDate(startDate, endDate),
      gender: Math.random() > 0.5 ? "male" : "female",
      question: details.questions[i % details.questions.length],
      expectedCategory: details.label
    });
  }
});

